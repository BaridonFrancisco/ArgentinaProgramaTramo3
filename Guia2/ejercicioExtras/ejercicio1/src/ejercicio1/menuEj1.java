    private void jBotonEliminarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotonEliminarTareaActionPerformed
        // TODO add your handling code here:
        Tarea valorCompletada=jlistTareasCompletadas.getSelectedValue(); // obtiene el valor  seleccionado de la lista
     
        if(valorCompletada!=null){
            listaTareasCompletadas.removeElement(valorCompletada);
            
        }
       Tarea valorPendiente=jLisTareasPendientes.getSelectedValue();
        if(valorPendiente!=null){
            listaTareasPendientes.removeElement(valorPendiente);
        }
    }//GEN-LAST:event_jBotonEliminarTareaActionPerformed

    private void jBotonCompletarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotonCompletarTareaActionPerformed
        // TODO add your handling code here:
        if(!jLisTareasPendientes.isSelectionEmpty()){
            Tarea tareaPendiente=jLisTareasPendientes.getSelectedValue();
            String nombreTarea=tareaPendiente.getTarea();
            listaTareasPendientes.removeElement(tareaPendiente);
            listaTareasCompletadas.addElement(new Tarea(nombreTarea,"Completada")); 
        }
    }//GEN-LAST:event_jBotonCompletarTareaActionPerformed

