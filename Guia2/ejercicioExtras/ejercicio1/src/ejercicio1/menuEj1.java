    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try{
         String nombreTarea=jNombreTarea.getText();
         if(nombreTarea.isEmpty()){
            throw new RuntimeException("Tarea vacia");
         }
         String estado;
         Tarea tarea;
        if(radidoButtonCompletada.isSelected()){
            estado="Completado";
            tarea=new Tarea(nombreTarea, estado);
            listaTareasCompletadas.addElement(tarea);
           
        }else{
            estado="Pendiente";
            tarea=new Tarea(nombreTarea, estado);
            listaTareasPendientes.addElement(tarea);
        }
        
        }catch(RuntimeException e){
            JOptionPane.showMessageDialog(this,"La tarea no puede estar vacia","Advertencia!!",JOptionPane.WARNING_MESSAGE);
            jNombreTarea.setText("");
        }
       
       
    }//GEN-LAST:event_jButton1ActionPerformed

